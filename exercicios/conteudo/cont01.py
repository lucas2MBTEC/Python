abelha = input('Digite algo: ')
print(abelha.isnumeric()) 
#pra testar numero
print(abelha.isalpha())
#pra testar letra
print(abelha.isalnum())
#testa numero ou letra
print(abelha.isupper())
#testa si tem letra maiuscula
print(abelha.isdecimal())
#verifica si tem numero decimal ou numero com virgula
print(abelha.islower())
#virifica si tem letras minusculas
print(abelha.isspace())
#para verificar si tem espaço